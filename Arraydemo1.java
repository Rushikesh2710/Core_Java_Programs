package Arrays;

public class Arraydemo1 {
	public  static  void main(String args[]) {
		int roll_no[]= {10,20,40,50,60};
		
		System.out.println("Array length is:"+roll_no.length);
		
		for(int index=0;index<roll_no.length;index++) {
			System.out.println(roll_no[index]);
		}
	}
	
}	
